			<h2>Bryce Dahlke, influenced by Avenged Sevenfold, Trivium, Bullet for My Valentine, and All That Remains, started playing guitar in 2007.  Joining a mariachi band in 2008, Bryce craved more metal.<br><br>He started writing music seen on TA's first album, Origins, in 2008.  He brought these tracks, which later became Breaking These Walls and Salvation Lies Within, when he joined TA in 2011 after seeing an ad in a music store in Andover, MN.  Bryce invited Jeremy Dobratz to join the band later in 2011.<br><br>Bryce continues to compose much of Trigger Armada's music, and has dabbled in other Minnesota metal projects over the years.</h2>
