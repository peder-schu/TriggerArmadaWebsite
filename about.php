			<h1>Trigger Armada</h1>
			<h2>Trigger Armada is a hard rock/metal band based out of Minneapolis, MN.  Originating out of Anoka, MN, TA has been playing shows since 2010 with Lead Singer and Bassist Peder Schuller, Lead Guitarist Bryce Dahlke, Rhythm Guitarist Jose Chinas, and Drummer and Vocalist Jeremy Dobratz.  <br><br>After dropping their first EP, "Event Horizon" in 2014, Trigger Armada took a year hiatus due to individual pursuits.  At the end of 2015, TA came back with 12 original songs, playing shows in the greater Twin Cities area and recording their first full length, self-produced album--Origins (released 1 Oct 2017).  <br><br>Trigger Armada is currently playing shows around the midwest and writing new music.</h2>
